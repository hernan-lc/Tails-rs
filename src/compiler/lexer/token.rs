use crate::errors::Span;

#[derive(Debug, Clone, PartialEq)]
pub struct SpannedToken {
    pub token: Token,
    pub span: Span,
}

#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Number(f64),
    String(String),
    BigInt(String),
    Regex(String),
    Identifier(String),
    TemplateLiteral(Vec<TemplatePart>),
    Const,
    Let,
    Var,
    Function,
    Return,
    If,
    Else,
    While,
    For,
    Do,
    Switch,
    Case,
    Break,
    Continue,
    New,
    Void,
    Delete,
    Typeof,
    Instanceof,
    Constructor,
    In,
    Of,
    Class,
    Extends,
    Super,
    This,
    Import,
    Export,
    Default,
    From,
    As,
    Type,
    Interface,
    Enum,
    Async,
    Await,
    Promise,
    Try,
    Catch,
    Finally,
    Throw,
    Static,
    Get,
    Set,
    Yield,
    Public,
    Private,
    Protected,
    Readonly,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Power,
    Assign,
    PlusAssign,
    MinusAssign,
    StarAssign,
    SlashAssign,
    PercentAssign,
    PowerAssign,
    AndAssign,
    OrAssign,
    XorAssign,
    BitAndAssign,
    BitOrAssign,
    Equal,
    StrictEqual,
    NotEqual,
    StrictNotEqual,
    Less,
    Greater,
    LessEqual,
    GreaterEqual,
    And,
    Or,
    Not,
    BitAnd,
    BitOr,
    BitXor,
    BitNot,
    ShiftLeft,
    ShiftRight,
    UnsignedShiftRight,
    Increment,
    Decrement,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    LeftBracket,
    RightBracket,
    Semicolon,
    Colon,
    Comma,
    Dot,
    Question,
    QuestionDot,
    NullishCoalescing,
    NullishCoalescingAssign,
    Arrow,
    Ellipsis,
    Hash,
    Eof,
}

#[derive(Debug, Clone, PartialEq)]
pub enum TemplatePart {
    Text(String),
    Expression(Vec<SpannedToken>),
}
